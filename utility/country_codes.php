function get_country_code_iso($code) {
	$ret_iso = '';
	foreach ($GLOBALS['country_codes'] as $item) {
		if ($item['code'] === $code) {
			$ret_iso = $item['iso'];
			break;
		}
	}
	return $ret_iso;
}

function get_country_code_preg_match($code) {
	switch (get_country_code_iso($code)) {
		case 'US':
		  return "/^$|([0-9]{5})(?:[-\s]*([0-9]{4}))?$/";
		case 'CA':
		  return "/^$|([A-Z][0-9][A-Z])\s*([0-9][A-Z][0-9])$/";
		case 'IE':
		  return "/(?:^$|[AC-FHKNPRTV-Y][0-9]{2}|D6W)[ -]?[0-9AC-FHKNPRTV-Y]{4}$/";
		case 'GB':
		  return "/^$|([Gg][Ii][Rr] 0[Aa]{2})|((([A-Za-z][0-9]{1,2})|(([A-Za-z][A-Ha-hJ-Yj-y][0-9]{1,2})|(([A-Za-z][0-9][A-Za-z])|([A-Za-z][A-Ha-hJ-Yj-y][0-9]?[A-Za-z])))) [0-9][A-Za-z]{2})$/";
		default:
		  return "/^$|(?:[A-Z0-9]+([- ]?[A-Z0-9]+)*)?$/";
	  }
}
